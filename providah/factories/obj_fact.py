class ObjFact:
    entries = {}

    @classmethod
    def register(cls, key, value):

        cls.entries[key] = value

    @classmethod
    def create(cls, key, **kwargs):

        if key not in cls.entries.keys():
            raise ValueError(f"key {key} not present in factory registry")

        return cls.entries[key](**kwargs)
